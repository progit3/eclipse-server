        private sealed class EclipseNewsAdminWindow : DefaultWindow
        {
            private readonly EclipseNewsEntry[] _entries;
            private readonly Action _onSaved;
            private readonly LineEdit[] _dateEdits = new LineEdit[3];
            private readonly LineEdit[] _titleEdits = new LineEdit[3];
            private readonly LineEdit[] _textEdits = new LineEdit[3];
            private readonly LineEdit[] _textureEdits = new LineEdit[3];

            public EclipseNewsAdminWindow(EclipseNewsEntry[] entries, Action onSaved)
            {
                _entries = entries;
                _onSaved = onSaved;

                Title = "Eclipse news";
                MinSize = new Vector2(620f, 500f);

                var root = new BoxContainer
                {
                    Orientation = BoxContainer.LayoutOrientation.Vertical,
                    SeparationOverride = 10,
                    Margin = new Thickness(12),
                    HorizontalExpand = true,
                    VerticalExpand = true,
                };

                for (var i = 0; i < entries.Length; i++)
                {
                    root.AddChild(BuildEditor(i));
                }

                var saveButton = new Button
                {
                    Text = "Save news",
                    MinHeight = 36,
                    HorizontalExpand = true,
                };
                saveButton.OnPressed += _ => SaveAndClose();
                root.AddChild(saveButton);

                Contents.AddChild(root);
            }

            private Control BuildEditor(int index)
            {
                var entry = _entries[index];
                var box = new BoxContainer
                {
                    Orientation = BoxContainer.LayoutOrientation.Vertical,
                    SeparationOverride = 4,
                    HorizontalExpand = true,
                };

                box.AddChild(new Label
                {
                    Text = $"News {index + 1}",
                    StyleIdentifier = MainMenuControl.StyleIdentifierGoldSmall,
                });

                _dateEdits[index] = AddLineEdit(box, "Date", entry.Date);
                _titleEdits[index] = AddLineEdit(box, "Title", entry.Title);
                _textEdits[index] = AddLineEdit(box, "Text", entry.Text);
                _textureEdits[index] = AddLineEdit(box, "Texture path", entry.TexturePath);

                return box;
            }

            private static LineEdit AddLineEdit(BoxContainer parent, string placeholder, string text)
            {
                var edit = new LineEdit
                {
                    PlaceHolder = placeholder,
                    Text = text,
                    HorizontalExpand = true,
                };
                parent.AddChild(edit);
                return edit;
            }

            private void SaveAndClose()
            {
                for (var i = 0; i < _entries.Length; i++)
                {
                    _entries[i].Date = _dateEdits[i].Text.Trim();
                    _entries[i].Title = _titleEdits[i].Text.Trim();
                    _entries[i].Text = _textEdits[i].Text.Trim();
                    _entries[i].TexturePath = _textureEdits[i].Text.Trim();
                }

                _onSaved();
                Close();
            }
        }
