        public void SetAccountInfo(string accountName, string roleName, int level, int currentExperience, int nextLevelExperience, int merits, int shards)
        {
            AccountName.Text = accountName;
            AccountTitle.Text = roleName;
            AccountRole.Text = EclipseProgression.GetRankName(level);
            AccountLevel.Text = $"Уровень {level}";
            AccountExperience.Text = $"{FormatAmount(currentExperience)} / {FormatAmount(nextLevelExperience)} XP";
            AccountExperienceProgress.Value = nextLevelExperience <= 0
                ? 0f
                : Math.Clamp((float) currentExperience / nextLevelExperience, 0f, 1f);
            MeritBalance.Text = FormatAmount(merits);
            ShardBalance.Text = FormatAmount(shards);
            StoreMeritBalance.Text = FormatAmount(merits);
            StoreShardBalance.Text = FormatAmount(shards);
        }

        private static string FormatAmount(int value)
        {
            return value.ToString("N0", CultureInfo.InvariantCulture).Replace(",", " ");
        }

