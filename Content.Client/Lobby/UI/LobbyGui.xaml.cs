        public void SwitchState(LobbyGuiState state)
        {
            DefaultState.Visible = false;
            CharacterSetupState.Visible = false;

            switch (state)
            {
                case LobbyGuiState.Default:
                    DefaultState.Visible = true;
                    ShowSection(_section);
                    break;
                case LobbyGuiState.CharacterSetup:
                    CharacterSetupState.Visible = true;
                    SectionPanel.Visible = false;
                    TopStatusPanel.Visible = false;
                    NewsPanel.Visible = false;
                    ServerActions.Visible = true;
                    RightSide.Visible = true;
                    BottomActions.Visible = true;
                    VoteContainer.Visible = false;

                    UserInterfaceManager.GetUIController<LobbyUIController>().ReloadCharacterSetup();

                    break;
            }

            ApplyEclipseLayout();
        }

        private void TogglePanel(bool value)
        {
            RightSide.Visible = value;
            ExpandPanel.Visible = !value;
        }

        protected override void FrameUpdate(FrameEventArgs args)
        {
            base.FrameUpdate(args);
            UpdateLaunchStatusAnimation(args.DeltaSeconds);
            ApplyEclipseLayout();
        }

        private void UpdateLaunchStatusAnimation(float deltaSeconds)
        {
            var target = _launchStatusTargetVisible ? 1f : 0f;
            _launchStatusVisibility = Math.Clamp(
                _launchStatusVisibility + MathF.Sign(target - _launchStatusVisibility) * deltaSeconds * LaunchStatusAnimationSpeed,
                0f,
                1f);

            if (MathF.Abs(_launchStatusVisibility - target) < 0.001f)
                _launchStatusVisibility = target;

            LaunchStatus.Visible = true;
            LaunchStatus.ReservesSpace = true;
            LaunchDivider.Visible = true;
            LaunchDivider.ReservesSpace = true;
            LaunchStatus.SetWidth = LaunchStatusWidth;
            LaunchStatus.Modulate = Color.White.WithAlpha(_launchStatusVisibility);
            LaunchDivider.Modulate = Color.White.WithAlpha(_launchStatusVisibility);
        }

        private void ApplyEclipseLayout()
        {
            var width = MathF.Max(MainContainer.Width, 1f);
            var height = MathF.Max(MainContainer.Height, 1f);
            var rightGap = width < NarrowBreakpoint ? NarrowEdgeGap : EdgeGap;
            var rightWidth = Math.Clamp(width - rightGap - CenterLeft, 320f, RightWidth);
            var rightStart = width - rightGap - rightWidth;
            var availableCenterWidth = rightStart - CenterLeft - 42f;
            var accountWidth = Math.Clamp(availableCenterWidth, 640f, AccountWidthWithLaunch);
            var newsWidth = Math.Min(NewsWidth, Math.Max(availableCenterWidth, 320f));

            SetLeftStretch(LeftSide, 0f, SidebarWidth);
            SetTopLeft(TopStatusPanel, CenterLeft, TopGap, accountWidth, AccountHeight);
            SetBottomLeft(NewsPanel, CenterLeft, NewsBottomGap, newsWidth, NewsHeight);
            SetTopRight(ServerActions, rightGap, TopGap, rightWidth, ServerActionsHeight);
            SetRightStretch(RightSide, rightGap, RightPanelTop, rightWidth, RightPanelBottomGap);
            SetTopRight(ExpandPanel, rightGap, RightPanelTop, 44f, 44f);
            SetBottomRight(BottomActions, rightGap, BottomGap, rightWidth, BottomActionsHeight);
            SetTopLeft(VoteContainer, CenterLeft, TopGap + AccountHeight + 12f, Math.Clamp(accountWidth, 360f, 640f), 180f);
            var centerRightReserve = RightSide.Visible ? rightGap + rightWidth + 28f : rightGap;
            SetTopLeft(SectionPanel, SidebarWidth, 0f, MathF.Max(width - SidebarWidth - centerRightReserve, 1f), height);

            var setupLeft = SidebarWidth + 28f;
            var setupWidth = Math.Max(width - setupLeft - centerRightReserve, 480f);
            SetTopLeft(CharacterSetupState, setupLeft, TopGap, setupWidth, MathF.Max(height - TopGap - BottomGap, 640f));
        }

