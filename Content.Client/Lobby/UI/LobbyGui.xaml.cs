            {
                MinSize = new Vector2(0f, 92f),
                HorizontalExpand = true,
                PanelOverride = StorePanel("#070300D8", "#A85E1238", 1, 4, 4),
            };
            imageWrap.AddChild(new TextureRect
            {
                TexturePath = item.TexturePath,
                Stretch = TextureRect.StretchMode.KeepAspectCentered,
                HorizontalExpand = true,
                VerticalExpand = true,
            });
            root.AddChild(imageWrap);

            if (!string.IsNullOrWhiteSpace(item.Badge))
            {
                root.AddChild(new Label
                {
                    Text = item.Badge,
                    StyleIdentifier = MainMenuControl.StyleIdentifierGoldSmall,
                    ClipText = true,
                });
            }

            root.AddChild(new Label
            {
                Text = item.Name,
                StyleIdentifier = MainMenuControl.StyleIdentifierText,
                ClipText = true,
            });
            root.AddChild(new Label
            {
                Text = item.Kind,
                StyleIdentifier = MainMenuControl.StyleIdentifierTinySubtle,
                ClipText = true,
            });

            var priceRow = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Horizontal,
                SeparationOverride = 5,
                HorizontalExpand = true,
            };
            priceRow.AddChild(new TextureRect
            {
                TexturePath = GetStoreCurrencyIcon(item.Currency),
                SetSize = item.Currency == StoreCurrency.Shards
                    ? new Vector2(20f, 20f)
                    : new Vector2(18f, 18f),
                Stretch = TextureRect.StretchMode.KeepAspectCentered,
            });
            priceRow.AddChild(new Label
            {
                Text = FormatAmount(item.Price),
                StyleIdentifier = MainMenuControl.StyleIdentifierGoldSmall,
                ClipText = true,
            });
            root.AddChild(priceRow);

            root.AddChild(new Control
            {
                VerticalExpand = true,
            });

            root.AddChild(new Button
            {
                Text = "\u041A\u0443\u043F\u0438\u0442\u044C",
                MinHeight = 30,
                HorizontalExpand = true,
                StyleIdentifier = item.Currency == StoreCurrency.Shards
                    ? MainMenuControl.StyleIdentifierPrimary
                    : MainMenuControl.StyleIdentifierSecondary,
            });

            return card;
        }

