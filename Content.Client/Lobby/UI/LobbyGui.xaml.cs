        private void BuildStoreCatalog()
        {
            StoreCatalogGrid.RemoveAllChildren();

            for (var i = 1; i < _storeItems.Length; i++)
            {
                StoreCatalogGrid.AddChild(BuildStoreCard(_storeItems[i]));
            }

            if (_storeItems.Length > 0)
                ApplyStoreFeatured(_storeItems[0]);
        }

        private void ApplyStoreFeatured(StoreItem item)
        {
            StoreFeaturedImage.TexturePath = item.TexturePath;
            StoreFeaturedCurrencyIcon.TexturePath = GetStoreCurrencyIcon(item.Currency);
            StoreFeaturedName.Text = item.Name;
            StoreFeaturedDescription.Text = item.Description;
            StoreFeaturedPrice.Text = FormatAmount(item.Price);
        }

        private Control BuildStoreCard(StoreItem item)
        {
            var card = new PanelContainer
            {
                MinSize = new Vector2(204f, 244f),
                SetSize = new Vector2(204f, 244f),
                PanelOverride = StorePanel("#070300F4", item.Currency == StoreCurrency.Shards ? "#7B2BB990" : "#A85E1290", 1, 9, 9),
            };

            var root = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Vertical,
                SeparationOverride = 5,
                HorizontalExpand = true,
                VerticalExpand = true,
            };
            card.AddChild(root);

