    }

    private void AppendBoolRow(string title, Func<bool?> getterSeedValue, Action<bool> setterSeedValue, Func<bool?> getterDnaDiskValue, Action<bool> setterDnaDiskValue)
    {
        AppendRow(SeedDnaConsoleWindowRow.Create(
            title,
            _seedIsPresent,
            _dnaDiskIsPresent,
            getterSeedValue: () => getterSeedValue(),
            getterDnaDiskValue: () => getterDnaDiskValue(),
