        ));
    }

    private void UpdateButtonsAll(SeedDnaConsoleBoundUserInterfaceState state)
    {
        if (state is not { IsSeedsPresent: true, IsDnaDiskPresent: true })
            return;

        if (state.SeedData != null)
            ExtractAllButton.Disabled = false;

        if (state.DnaDiskData != null)
            ReplaceAllButton.Disabled = false;
    }

    private void SubmitUpdateDto(TargetSeedData target)
    {
        _owner.SubmitData(target, target == TargetSeedData.Seed ? _seedDataDto! : _dnaDiskDataDto!);
    }

    private void RefreshRows()
    {
        foreach (var row in _allRows)
        {
            row.RefreshValues();
        }
    }

    private void AppendRow(SeedDnaConsoleWindowRow? row)
    {
        if (row == null)
            return;

        row.IncludeToContainer(MutationContainer);
        _allRows.Add(row);
    }

    private static float? GetGasValue(Dictionary<Gas, float>? data, Gas gas)
    {
        return data != null && data.TryGetValue(gas, out var value) ? value : null;
    }

    private static void SetGasValue(SeedDataDto dto, Gas gas, float value, bool consume)
    {
        if (consume)
        {
            dto.ConsumeGasses ??= new Dictionary<Gas, float>();
            dto.ConsumeGasses[gas] = value;
            return;
        }

        dto.ExudeGasses ??= new Dictionary<Gas, float>();
        dto.ExudeGasses[gas] = value;
    }

    private string GetChemicalName(string chemicalName)
    {
        if (_prototypeManager.TryIndex<ReagentPrototype>(chemicalName, out var reagent))
            return reagent.LocalizedName;

        if (_localizationManager.TryGetString($"seed-dna-chemical-{chemicalName}", out var localized))
            return localized;

        return chemicalName;
    }

    private string GetGasName(Gas gas)
    {
        var key = gas switch
        {
            Gas.Oxygen => "gas-oxygen",
            Gas.Nitrogen => "gas-nitrogen",
            Gas.CarbonDioxide => "gas-carbon-dioxide",
            Gas.Plasma => "gas-plasma",
            Gas.Tritium => "gas-tritium",
            Gas.WaterVapor => "gas-water-vapor",
            Gas.Ammonia => "gas-ammonia",
            Gas.NitrousOxide => "gas-nitrous-oxide",
            Gas.Frezon => "gas-frezon",
            _ => null
        };

        return key != null && _localizationManager.TryGetString(key, out var localized)
            ? localized
            : gas.ToString();
    }
}
