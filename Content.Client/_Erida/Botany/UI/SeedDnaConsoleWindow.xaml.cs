    }

    private void AppendEnumRow(string title, Func<SharedHarvestTypeDto?> getterSeedValue, Action<SharedHarvestTypeDto> setterSeedValue, Func<SharedHarvestTypeDto?> getterDnaDiskValue, Action<SharedHarvestTypeDto> setterDnaDiskValue)
    {
        AppendRow(SeedDnaConsoleWindowRow.Create(
            title,
            _seedIsPresent,
